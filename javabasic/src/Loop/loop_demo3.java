package Loop;

public class loop_demo3 {

	public static void main(String[] args) {
		for(int i=0; i<10; i++) {
			System.out.println("Index:"+i+"Hello i am..");
			}
		for(int i=1; i<10; i++) {
			System.out.println(i);
		}
		System.out.println("*************Reverse***");
		for(int i=10; i>0; i--) {
			System.out.println(i);
		}
		System.out.println("*************A to zprint***");
		for(char c1='A'; c1<='Z'; c1++) {
			System.out.println(c1);
		}
		
	  }

}
